num1 = float(input("Enter the first number"))
num2 = float(input("Enter the second number"))

sum = num1 + num2
product = num1 * num2
division = num1 / num2
modulo = num1 % num2
integerquotient = int(num1//num2)

print(f"Sum of {num1} and {num2} = {sum}")
print(f"Product of {num1} and {num2} = {product}")
print(f"Division of {num1} and {num2} = {division}")
print(f"Modulo of {num1} and {num2} = {modulo}")
print(f"Integer quotient of {num1} and {num2} = {integerquotient}")